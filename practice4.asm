.model small
.stack 100h

.data
    array dw 10 dup (20 dup (?))  ; Двовимірний масив 10x20

.code
main:
    mov ax, @data       ; Завантаження адреси сегменту даних в регістр AX
    mov ds, ax          ; Ініціалізація DS

    xor cx, cx          ; CX - лічильник рядків масиву (початкове значення 0)
    lea di, array       ; Завантаження адреси масиву в регістр DI

outer_loop:
    xor bx, bx          ; Початкове значення X (стовпчик, починається з 0)
    mov dx, cx          ; DX - номер рядка

inner_loop:
    mov ax, dx          ; AX = Y
    add ax, 5           ; AX = Y + 5
    mul bx              ; AX = AX * X
    mov [di], ax        ; Записати результат у поточний елемент масиву
    add di, 1           ; Перейти до наступного елементу масиву
    inc bx              ; Збільшити X (стовпчик)
    cmp bx, 20          ; Перевірка, чи завершено обробку всіх стовпчиків
    jl inner_loop       ; Якщо ні, продовжити обробку стовпчиків

    inc cx              ; Збільшити лічильник рядків
    cmp cx, 10          ; Перевірка, чи завершено обробку всіх рядків
    jl outer_loop       ; Якщо ні, продовжити обробку рядків

    ; Додатковий код може бути доданий тут

    mov ah, 4ch         ; Код завершення програми DOS
    int 21h

end main














